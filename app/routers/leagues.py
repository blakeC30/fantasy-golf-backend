@router.get("/{league_id}", response_model=LeagueOut)
def get_league(
    league_and_member: tuple[League, LeagueMember] = Depends(require_league_member),
):
    """Return league details including invite_code. Requires approved membership."""
    league, _ = league_and_member
    return league


@router.get("/{league_id}/members", response_model=list[LeagueMemberOut])
def list_members(
    league_and_member: tuple[League, LeagueMember] = Depends(require_league_member),
    db: Session = Depends(get_db),
):
    """List approved members of a league. Requires membership."""
    league, _ = league_and_member
    return (
        db.query(LeagueMember)
        .filter_by(league_id=league.id, status=LeagueMemberStatus.APPROVED.value)
        .options(joinedload(LeagueMember.user))
        .all()
    )


@router.patch("/{league_id}/members/{user_id}/role", response_model=LeagueMemberOut)
def update_member_role(
    user_id: uuid.UUID,
    body: RoleUpdate,
    league_and_admin: tuple[League, LeagueMember] = Depends(require_league_admin),
    db: Session = Depends(get_db),
):
    """Change an approved member's role. Requires league admin."""
    league, _ = league_and_admin

    if body.role not in (LeagueMemberRole.ADMIN.value, LeagueMemberRole.MEMBER.value):
        raise HTTPException(status_code=400, detail="Role must be 'admin' or 'member'")

    membership = (
        db.query(LeagueMember)
        .filter_by(league_id=league.id, user_id=user_id, status=LeagueMemberStatus.APPROVED.value)
        .options(joinedload(LeagueMember.user))
        .first()
    )
    if not membership:
        raise HTTPException(status_code=404, detail="Member not found")

    membership.role = body.role
    db.commit()
    db.refresh(membership)
    return membership


@router.delete("/{league_id}/members/{user_id}", status_code=204)
def remove_member(
    user_id: uuid.UUID,
    league_and_admin: tuple[League, LeagueMember] = Depends(require_league_admin),
    db: Session = Depends(get_db),
):
    """Remove an approved member from a league. Requires league admin."""
    league, admin_membership = league_and_admin

    if user_id == admin_membership.user_id:
        raise HTTPException(status_code=400, detail="You cannot remove yourself from the league")

    membership = db.query(LeagueMember).filter_by(
        league_id=league.id, user_id=user_id, status=LeagueMemberStatus.APPROVED.value
    ).first()
    if not membership:
        raise HTTPException(status_code=404, detail="Member not found")

    db.delete(membership)
    db.commit()


